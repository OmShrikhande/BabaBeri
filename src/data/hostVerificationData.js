// Helper function to get hosts by status
export const getHostsByStatus = (status) => {
  return hostVerificationData.filter(host => host.status.toLowerCase() === status.toLowerCase());
};

// Helper function to get pending hosts count
export const getPendingHostsCount = () => {
  return hostVerificationData.filter(host => host.status.toLowerCase() === 'pending').length;
};

// Helper function to search hosts
export const searchHosts = (searchTerm) => {
  if (!searchTerm) return hostVerificationData;
  
  return hostVerificationData.filter(host =>
    host.hostId.toLowerCase().includes(searchTerm.toLowerCase()) ||
    host.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
    host.email.toLowerCase().includes(searchTerm.toLowerCase())
  );
};