// Helper function to get rank badge color
export const getRankBadgeColor = (rank) => {
  if (rank === 1) return 'bg-gradient-to-r from-yellow-400 to-yellow-600 text-black';
  if (rank === 2) return 'bg-gradient-to-r from-gray-300 to-gray-500 text-black';
  if (rank === 3) return 'bg-gradient-to-r from-orange-400 to-orange-600 text-white';
  if (rank <= 10) return 'bg-gradient-to-r from-[#F72585] to-[#7209B7] text-white';
  return 'bg-gray-700 text-gray-300';
};

// Helper function to get level badge color
export const getLevelBadgeColor = (level) => {
  if (level.includes('Diamond')) return 'bg-blue-600/20 text-blue-400 border-blue-600/30';
  if (level.includes('VIP Gold')) return 'bg-yellow-600/20 text-yellow-400 border-yellow-600/30';
  if (level.includes('Gold')) return 'bg-yellow-600/20 text-yellow-400 border-yellow-600/30';
  if (level.includes('VIP Silver')) return 'bg-gray-400/20 text-gray-300 border-gray-400/30';
  if (level.includes('Silver')) return 'bg-gray-400/20 text-gray-300 border-gray-400/30';
  if (level.includes('VIP Bronze')) return 'bg-orange-600/20 text-orange-400 border-orange-600/30';
  if (level.includes('Bronze')) return 'bg-orange-600/20 text-orange-400 border-orange-600/30';
  return 'bg-gray-600/20 text-gray-400 border-gray-600/30';
};